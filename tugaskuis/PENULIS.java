package PENULIS;

public class PENULIS {

    private String nama;
    private String motivasi;
    private String umur;

    public PENULIS (String nama, String motivasi, String umur) {
        this.nama = nama;
        this.motivasi = motivasi;
        this.umur = umur;
    }

    public String getNama() {
        return nama;
    }

    public String getMotivasi() {
        return motivasi;
    }

    public String getUmur() {
        return umur;
    }

}
